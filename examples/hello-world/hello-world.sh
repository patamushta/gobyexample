# Для запуска программы, сохраните текст программы в файле
# `hello-world.go` и используйте утилиту `go run`.
$ go run hello-world.go
hello world

# `go build` компилирует и собирает программу в
# исполняемый файл.
$ go build hello-world.go
$ ls
hello-world	hello-world.go

# После сборки можно запустить программу напрямую.
$ ./hello-world
hello world

